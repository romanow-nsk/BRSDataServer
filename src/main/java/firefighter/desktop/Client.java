    @Override
    public void logOff(){
        if (secondForm){
            dispose();
            return;
            }
        for(I_PanelEvent xx : panels)
            xx.shutDown();
        try {
            Response<JEmpty> res = service.logoff(debugToken).execute();
            if (!res.isSuccessful()){
                System.out.println("Ошибка сервера: "+ Utils.httpError(res));
                }
            loginForm.disConnect();
            login();
            }catch (Exception ee){
                System.out.println("Ошибка сервера: "+ee.toString());
                loginForm.disConnect();
                login();
                }
        }
