    public void startUser(){
        try {
            setTitle("Mongo-сервер данных: "+loginUser.getHeader());
            debugToken = loginUser.getSessionToken();
            setMES(MES);
            ShowLog.setSelected(true);
            setBounds(100, 50, fullView, ViewHight);
            PanelList.setBounds(10,10,BasePanel.PanelW,BasePanel.PanelH);
            MES.setBounds(BasePanel.PanelW+10,10,mesW,BasePanel.PanelH);
            ShowLog.setSelected(true);
            PanelList.removeAll();
            panels.clear();
            WorkSettings ws = new APICall2<WorkSettings>(){
                @Override
                public Call<WorkSettings> apiFun() {
                    return service.workSettings(debugToken);
                    }
                }.call(this);
            for(PanelDescriptor pp : panelDescList){
                boolean bb=false;
                boolean editMode = true;
                for(int vv : pp.userTypes)
                    if (Math.abs(vv)==loginUser.getTypeId()){
                        bb=true;
                        editMode = vv > 0;
                        break;
                    }
                if (bb){
                    BasePanel panel = (BasePanel) pp.view.newInstance();
                    panel.editMode = editMode;
                    panel.initPanel(this);
                    panels.add(panel);
                    PanelList.add(pp.name, panel);
                    }
                }

            BasePanel pn;
            refresh();
            } catch(Exception ee){
                System.out.println(ee.toString());
                ee.printStackTrace();
                }
        }
    public void refresh(){
        refreshMode=true;
        for(I_PanelEvent xx : panels)
            xx.refresh();
        refreshMode=false;
        }

